/* 4) Utilize if/else para escrever um código que, dado um valor recebido como parâmetro, retorne “positive” se esse valor for positivo, “negative” se for negativo, e caso não seja nem positivo e nem negativo retorne “zero”. */

const a = -6; 
const b = 9;
const c = 0;

if(a > 0) {
  console.log('A é positivo');
} else if (a < 0) {
  console.log('A é negativo');
} else {
  console.log('A vale 0');
}

if(b > 0) {
  console.log('B é positivo');
} else if (b < 0) {
  console.log('B é negativo');
} else {
  console.log('B vale 0');
}

if(c > 0) {
  console.log('C é positivo');
} else if (c < 0) {
  console.log('C é negativo');
} else {
  console.log('C vale 0');
}